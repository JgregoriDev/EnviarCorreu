    public void llistaArchiusPDF(File[] llista) {
        String format = ".pdf";
        for (File file : llista) {
            //Obtinc format archiu
            String FormatArchiu = file.getName().substring(file.getName().length() - 4, file.getName().length());
            if (FormatArchiu.equals(format)) {
                llistaPDF.add(file);
            }
        }
        System.out.println("llistapdf=" + llistaPDF.size());
    }

